defmodule Tasks.Todo do
  @moduledoc """
  The Todo context.
  """

  import Ecto.Query, warn: false
  alias Tasks.Repo

  alias Tasks.Todo.Task

  @doc """
  Returns the list of tasks.

  ## Examples

      iex> list_tasks()
      [%Task{}, ...]

  """
  def list_tasks do
    Repo.all(Task)
    |> Enum.reverse()
    |> Repo.preload(:user)
  end

  @doc """
  Gets a single task.

  Raises `Ecto.NoResultsError` if the Task does not exist.

  ## Examples

      iex> get_task!(123)
      %Task{}

      iex> get_task!(456)
      ** (Ecto.NoResultsError)

  """
  def get_task!(id) do
    Repo.get!(Task, id)
    |> Repo.preload(:user)
  end

  alias Tasks.Todo.Block

  def blocks_map_for(task_id) do
    Repo.all(from b in Block,
      where: b.task_id == ^task_id)
      |> Enum.map(&({&1.block_id.start, &1.block_id.end}))
      |> Enum.into(%{})
  end

  @doc """
  Creates a task.

  ## Examples

      iex> create_task(%{field: value})
      {:ok, %Task{}}

      iex> create_task(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_task(attrs \\ %{}) do
    %Task{}
    |> Task.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a task.

  ## Examples

      iex> update_task(task, %{field: new_value})
      {:ok, %Task{}}

      iex> update_task(task, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_task(%Task{} = task, attrs) do
    task
    |> Task.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Task.

  ## Examples

      iex> delete_task(task)
      {:ok, %Task{}}

      iex> delete_task(task)
      {:error, %Ecto.Changeset{}}

  """
  def delete_task(%Task{} = task) do
    Repo.delete(task)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking task changes.

  ## Examples

      iex> change_task(task)
      %Ecto.Changeset{source: %Task{}}

  """
  def change_task(%Task{} = task) do
    Task.changeset(task, %{})
  end

  alias Tasks.Todo.Manage

  @doc """
  Returns the list of manager.

  ## Examples

      iex> list_manager()
      [%Manage{}, ...]

  """
  def list_manager do
    Repo.all(Manage)
  end

  #Taken from Nat Tuck's lecture notes
  def follows_map_for(user_id) do
    Repo.all(from m in Manage,
             where: m.manager_id == ^user_id)
             |> Enum.map(&({&1.managee_id, &1.id}))
             |> Enum.into(%{})
  end

  #Taken from Nat Tuck's lecture notes
  def feed_tasks_for(user) do
    user = Repo.preload(user, :managees)
    managed_ids = Enum.map(user.managees, &(&1.id))

    Repo.all(Task)
    |> Enum.filter(&(Enum.member?(managed_ids, &1.user_id)))
    |> Repo.preload(:user)
  end

  def managees_by_user(user) do
    user = Repo.preload(user, :managees)
    Enum.map(user.managees, fn(x) ->
              { x.name, x.id }
            end)
  end

  def managers_by_user(user) do
    user = Repo.preload(user, :managers)
    Enum.map(user.managers, fn(x) ->
              { x.name, x.id }
            end)
  end

